export const messages: Record<Locale, Record<MessageKey, string>> = {
  "en": en,
  "zh-CN": zhCN
};

export type MessageVars = Record<string, string | number>;

function formatTemplate(template: string, vars?: MessageVars) {
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (full, key: string) => {
    if (!(key in vars)) return full;
    const value = vars[key];
    if (value == null) return full;
    return typeof value === "number" ? String(value) : value;
  });
}

export function t(locale: Locale, key: MessageKey, vars?: MessageVars) {
  const template = messages[locale][key];
  return formatTemplate(template, vars);
}

export function normalizeLocale(value: string | null | undefined): Locale | null {
  if (!value) return null;
  const v = value.trim().toLowerCase();
  if (v === "en" || v.startsWith("en-")) return "en";
  if (v === "zh" || v.startsWith("zh-")) return "zh-CN";
  return null;
}

export function detectLocaleFromAcceptLanguage(acceptLanguage: string | null | undefined): Locale {
  const raw = (acceptLanguage ?? "").trim();
  if (!raw) return "en";
  const parts = raw.split(",");
  for (const part of parts) {
    const tag = part.split(";")[0]?.trim();
    const normalized = normalizeLocale(tag);
    if (normalized) return normalized;
  }
  return "en";
}
